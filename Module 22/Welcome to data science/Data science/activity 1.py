
import numpy as np
a = np.array([3,3,6])
print('First array:')
print(a)
print('\n')

b = np.array([10, 10, 10])
print('Second array: ')
print(b)
print('\n')

print('Add the two arrays: ')
print(np.add(a,b))
print('\n')

print('Subtract the two arrays:')
print(np.subtract(a,b))
print('\n')

print(' Multiply the two arrays: ')
print(np.divide(a,b))
print('\n')